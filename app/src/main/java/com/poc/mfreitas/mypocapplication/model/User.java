package com.poc.mfreitas.mypocapplication.model;

public class User {

    private Integer id;
    private String name;
    private String username;
    private String email;

    public Integer getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getUsername() {
        return username;
    }

    public String getEmail() {
        return email;
    }
}
